# Remove Search cache
def remove_cache(show_popup: bool = False, parent=None):
    logging.debug("Starting cleaning Cache..")
    for file in os.listdir(CACHED_SEARCHES_FOLDER):
        os.remove(os.path.join(CACHED_SEARCHES_FOLDER, file))
    if show_popup:
        FF_Additional_UI.msg.show_info_messagebox("Cleared Cache", "Cleared Cache successfully!", parent)
    logging.info("Cleared Cache successfully!\n")


# Test if Cache should be deleted
def cache_test(is_launching):
    logging.debug("Testing if cache should be deleted..")

    # Loading Settings File
    with open(os.path.join(FF_LIB_FOLDER, "Settings"), "rb") as SettingsFile:
        # Taking the item four because it is the Cache Setting
        cache_settings = load(SettingsFile)["cache"]
        logging.debug(f"{cache_settings = }")

    # Deleting Cache on Launch
    if cache_settings == "On Launch" and is_launching:
        logging.debug("Deleting Cache!")
        for file in os.listdir(CACHED_SEARCHES_FOLDER):
            os.remove(os.path.join(CACHED_SEARCHES_FOLDER, file))

    # Deleting Cache after a Day
    elif cache_settings == "after a Day":
        SECONDS_OF_A_DAY = 86400

        # Looping through every File in CACHED_SEARCHES_FOLDER
        for file in os.listdir(CACHED_SEARCHES_FOLDER):
            if os.stat(os.path.join(CACHED_SEARCHES_FOLDER, file)).st_birthtime <= time() - SECONDS_OF_A_DAY:
                logging.debug(f"Deleting Cache for File: {file}")
                os.remove(os.path.join(CACHED_SEARCHES_FOLDER, file))

    # Deleting Cache after a Week
    elif cache_settings == "after a Week":
        SECONDS_OF_A_WEEK = 604800

        # Looping through every File in CACHED_SEARCHES_FOLDER
        for file in os.listdir(CACHED_SEARCHES_FOLDER):
            if os.stat(os.path.join(CACHED_SEARCHES_FOLDER, file)).st_birthtime <= time() - SECONDS_OF_A_WEEK:
                logging.debug(f"Deleting Cache for File: {file}")
                os.remove(os.path.join(CACHED_SEARCHES_FOLDER, file))

    # Skipping
    else:
        logging.debug("Skipping deleting...")

    logging.debug("Finished Cache Testing!\n")


# Function to get the File Size of a directory
def get_file_size(file: str) -> int:
    if os.path.isdir(file):
        file_size_list_obj = 0
        # Gets the size
        for path, dirs, files in os.walk(file):
            for file in files:
                try:
                    if not os.path.islink(file):
                        file_size_list_obj += os.path.getsize(os.path.join(path, file))
                except FileNotFoundError or ValueError:
                    continue
    elif os.path.isfile(file):
        try:
            file_size_list_obj = os.path.getsize(file)
        except FileNotFoundError or ValueError:
            return -1
    else:
        return -1
    if os.path.islink(file):
        return -2
    else:
        return file_size_list_obj


# Used for converting file names so that they can be used in the terminal
# e.g.: "/Users/admin/Test File" = "/Users/admin/Test\ File"
def convert_file_name_for_terminal(name):
    return name.replace(" ", r"\ ")


# Convert File Size to a String
def conv_file_size(byte_size: int) -> str:
    logging.debug("Called conv_file_size")
    if byte_size == -1:
        return "ERROR! (File does not exist or isn't valid)"
    elif byte_size == -2:
        return "ERROR! (File is a Link to an other File)"
    elif byte_size > 1000000000:
        return f"{round(byte_size / 1000000000, 2)} GB"
    elif byte_size > 1000000:
        return f"{round(byte_size / 1000000, 2)} MB"
    elif byte_size > 1000:
        return f"{round(byte_size / 1000, 2)} KB"
    else:
        return f"{byte_size} Bytes"


# Function for nicely displaying Paths
def display_path(path: str, length_of_first_part_of_string: int = 40):
    if len(path) >= length_of_first_part_of_string + 13:
        return f"{path[0:length_of_first_part_of_string]}...{path[len(path) - 20:len(path)]}"
    else:
        return path


